def _hex_to_rgba(hex_color: str) -> RGBAColor:
    h = hex_color.lstrip("#")
    r, g, b = int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)
    return (r / 255.0, g / 255.0, b / 255.0, 1.0)


def assign_colors(extensions: list[str], colormap: str = "tab20") -> dict[str, RGBAColor]:
    """Map each unique extension to a stable RGBA colour from *colormap*.

    The mapping is determined by an MD5 hash of the extension name so that
    the same extension always receives the same colour regardless of which
    other extensions are present.

    When *colormap* is the default ("tab20"), known extensions are coloured
    using the GitHub Linguist palette. When a custom colormap is supplied,
    all extensions are coloured from that colormap so the flag is fully respected.

    Args:
        extensions: All file extensions found in the tree (may contain duplicates).
        colormap: Matplotlib colormap name.

    Returns:
        Mapping from extension string to RGBA tuple.
    """
    import hashlib

    use_linguist = colormap == "tab20"
    cmap: Any = plt.get_cmap(colormap)
    n: int = int(cmap.N) if hasattr(cmap, "N") else 256
    result: dict[str, RGBAColor] = {}
    for ext in set(extensions):
        if use_linguist and ext.lower() in _LINGUIST:
            result[ext] = _hex_to_rgba(_LINGUIST[ext.lower()])
        else:
            result[ext] = tuple(cmap((int(hashlib.md5(ext.encode()).hexdigest(), 16) % n) / n))
    return result
